
public class One {

	public static void main(String[] args) {

		int a = 4;
		int b = 2;
		
		System.out.println(a+" + "+b+" = "+(a + b));
		System.out.println(a+" - "+b+" = "+(a - b));
		System.out.println(a+" * "+b+" = "+(a * b));
		System.out.println(a+" / "+b+" = "+(a / b));
		System.out.println(a+" % "+b+" = "+(a % b));

	}

}
